package devlaunchers.structuresystem.shapes;

public class ShapeSize {

	public int minX, maxX;
	public int minY, maxY;
	public int minZ, maxZ;
	
	public ShapeSize(int minX, int maxX, int minY, int maxY, int minZ, int maxZ) {
		this.minX = minX;
		this.maxX = maxX;
		this.minY = minY;
		this.maxY = maxY;
		this.minZ = minZ;
		this.maxZ = maxZ;
	}
	
}
